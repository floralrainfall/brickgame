use bevy::color::Srgba;

pub fn brick_color_to_srgb(color: i32) -> Srgba {
    match color {
        1 => Srgba::new(242.0 / 255.0, 243.0 / 255.0, 243.0 / 255.0, 1.0),
        2 => Srgba::new(161.0 / 255.0, 165.0 / 255.0, 162.0 / 255.0, 1.0),

        6 => Srgba::new(194.0 / 255.0, 218.0 / 255.0, 184.0 / 255.0, 1.0),

        28 => Srgba::new(40.0 / 255.0, 127.0 / 255.0, 71.0 / 255.0, 1.0),
        29 => Srgba::new(161.0 / 255.0, 196.0 / 255.0, 140.0 / 255.0, 1.0),
        37 => Srgba::new(75.0 / 255.0, 151.0 / 255.0, 75.0 / 255.0, 1.0),

        _ => Srgba::new(1.0, 0.0, 1.0, 1.0),
    }
}
